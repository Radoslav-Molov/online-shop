const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const cartSchema = new Schema({
  image: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = Cart = mongoose.model("cart", cartSchema);
